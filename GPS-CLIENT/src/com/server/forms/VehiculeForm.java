    public void load() throws NamingException {
       
        List<Vehicule> listeVehicules = this.cnx.getVehiculeProxyType().findAll();

        this.tab = (DefaultTableModel) vtab.getModel();
        this.tab.setRowCount(0);
        for (Vehicule v : listeVehicules) {
            tab.addRow(new Object[]{
                v.getId(),
                v.getMatricule(),
                v.getMarque(),
                v.getType()
            });
        }
    }
