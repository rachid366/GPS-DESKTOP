    private void vtabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vtabMouseClicked
        // TODO add your handling code here:
        this.id=Integer.parseUnsignedInt(tab.getValueAt(vtab.getSelectedRow(), 0).toString());
        
        txtid.setText(tab.getValueAt(vtab.getSelectedRow(), 0).toString());
        txtmatricule.setText(tab.getValueAt(vtab.getSelectedRow(), 1).toString());
        txtmarque.setText(tab.getValueAt(vtab.getSelectedRow(), 2).toString());
        txttype.setText(tab.getValueAt(vtab.getSelectedRow(), 3).toString());

    }//GEN-LAST:event_vtabMouseClicked

